    public int  cancelar(){
        
        int i = 5;
        if ((!txtTitulo.getText().equals("")) || (!txtAutor.getText().equals("")) ||
                (!txtEditora.getText().equals("")) || (!txtValor.getText().equals(""))
                || (!txtCodigo.getText().equals(""))        ){
            
        
            i = JOptionPane.showConfirmDialog(null, "Cancelar Cadastro?");
            
            if(i == 0){
            
                txtTitulo.setText("");
                txtAutor.setText("");
                txtEditora.setText("");
                txtValor.setText("");
                txtCodigo.setText("");
                
                JOptionPane.showMessageDialog(null, "Cadastro Cancelado");
               
            
            }
        
        
        }
        return i;
    }
    
    public void voltar(){
        
        int i = cancelar();
        
        if (txtTitulo.getText().equals("") && txtAutor.getText().equals("") &&
                txtEditora.getText().equals("") && txtValor.getText().equals("") &&
                txtCodigo.getText().equals("")){
        setVisible(Boolean.FALSE);
        OpcaoCadastro oc = null;
            try {
                oc = new OpcaoCadastro();
            } catch (Exception ex) {
                Logger.getLogger(CadastrarLivro.class.getName()).log(Level.SEVERE, null, ex);
            }
        oc.setInter(lc.getInter());
        oc.setVisible(Boolean.TRUE);
        
        
        }
        
        
        
        
        else if(i == 0){
        setVisible(Boolean.FALSE);
        OpcaoCadastro oc = null;
            try {
                oc = new OpcaoCadastro();
            } catch (Exception ex) {
                Logger.getLogger(CadastrarLivro.class.getName()).log(Level.SEVERE, null, ex);
            }
        oc.setInter(lc.getInter());
        oc.setVisible(Boolean.TRUE);
        }
    }
    

