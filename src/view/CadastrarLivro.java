    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        
        
        
        
        try{
        
        if(txtTitulo.getText().trim().equals("")){
            throw new Exception ("Campo Título não pode estar vazio");
        
        }
        if(txtAutor.getText().trim().equals("")){
           throw new Exception ("Campo Autor não pode estar vazio");
        
        }
        if(txtValor.getText().trim().equals("")){
             throw new Exception ( "Campo Valor não pode estar vazio");
        
        }
        if(txtEditora.getText().trim().equals("")){
             throw new Exception ( "Campo Editora não pode estar vazio");
        
        }
        if(txtCodigo.getText().trim().equals("")){
             throw new Exception ( "Campo Código não pode estar vazio");
        
        }else{
            
            
            
         
            
         
            
            try{
            lc.cadastrarLivro(txtTitulo.getText().trim(), txtAutor.getText().trim()
                ,txtEditora.getText().trim(),Double.parseDouble(txtValor.getText()), 
                Integer.parseInt(txtCodigo.getText().trim()));
            
            JOptionPane.showMessageDialog(null, "Livro Cadastrado com sucesso!");
            
            txtTitulo.setText("");
            txtAutor.setText("");
            txtValor.setText("");
            txtCodigo.setText("");
            txtEditora.setText("");
            
            voltar();
            
            
            }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());}
        }
        
                }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());}
        
        
        
        
                
              
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

