    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(lista.size()!=0){
            int i = JOptionPane.showConfirmDialog(null,"Deseja Cancelar a Venda?");
            if(i == 0){
                
                vc.adicionarArrayLivros(lista);
                limpaTabela();
                cpf.setText("");
             
                codigo.setText("");
                valor.setText("");
               
                
                JOptionPane.showMessageDialog(null, "Venda Cancelada.");
                try {
                    volta();
                } catch (Exception ex) {
                    Logger.getLogger(InterVendaCartao.class.getName()).log(Level.SEVERE, null, ex);
                }
              
            }
        }else{
        
        cpf.setText("");
        codigo.setText("");
        valor.setText("");
        limpaTabela();
        JOptionPane.showMessageDialog(null, "Venda Cancelada.");
            try {
                volta();
            } catch (Exception ex) {
                Logger.getLogger(InterVendaCartao.class.getName()).log(Level.SEVERE, null, ex);
            }
       
        }
                                          
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         if(lista.size()>0 && cartaoCadastrado){
        
          
          cliente = vc.getCliente(cpf.getText());
                //  f.getLivraria().getListaClientes().get(cpf.getText());
          
          int cod = vc.geraCodigo();
         int parcelas = getParcelas();
        
          Venda v = new VendaCartao(cod,FormaDePagamento.Cartao, cliente, 
                  Double.parseDouble(valor.getText())
                , lista, data, cartao, parcelas);
        
         vc.efetuarVenda(v);
        
        JOptionPane.showMessageDialog(null,"Venda Realizada.");
        
        cpf.setText("");
        codigo.setText("");
        valor.setText("");
        limpaTabela();
       
             try {
                 volta();
             } catch (Exception ex) {
                 Logger.getLogger(InterVendaCartao.class.getName()).log(Level.SEVERE, null, ex);
             }
       
       
        }else{
            JOptionPane.showMessageDialog(null, "Verifique a lista de Compra e/ou Cadastro do Cartão.");
        
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

