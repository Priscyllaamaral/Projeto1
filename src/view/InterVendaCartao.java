    public void trocaInformacao(ArrayList<Livro> array, String cpf1, double valorC){
      
        lista = array;
        cpfCompra = cpf1;
        valorCompra = valorC;
     
        
        insereNaTabela(array);
      
        numero.setText(Long.toString(cartao.getNumero()));
        valor.setText(Double.toString(valorC));
        cpf.setText(cpf1);
        setParcelas(valorCompra);
 
    }
    
