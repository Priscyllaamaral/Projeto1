    public void insereNaTabela(ArrayList<Livro> array){
    
        for (int i = 0; i < array.size(); i++) {
            Object [] linha = {array.get(i).getTitulo()};
            modelo.addRow(linha);
        }

    }
    
     public int getParcelas(){
    
    if(parcela.getSelectedItem() == null){ return 0;}
    
    return Integer.parseInt(parcela.getSelectedItem().toString());
    
   
    }
    
    
    public void setParcelas(double valor){
         
        
        if(cartao.getTipo() == TipoVendaCartao.CRÉDITO){
         
         if(valor >= 100.00 && valor < 200.00){
              
                parcela.addItem("1");
                parcela.addItem("2");
                parcela.addItem("3");
         
         }
          if(valor >= 200.00 && valor<300.00){
               parcela.addItem("1");
         parcela.addItem("2");
         parcela.addItem("3");
         parcela.addItem("4");
         
         }
          if(valor >= 300.00){
               parcela.addItem("1");
         parcela.addItem("2");
         parcela.addItem("3");
         parcela.addItem("4");
         parcela.addItem("5");
         }
        }
         
     }
    
    
    
