    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed

       
        
        
        if(vc.getTamArrayLivros(Integer.parseInt(codigo.getText())) > 0){
            double valor2 = vc.getArrayLivros(Integer.parseInt(codigo.getText())).get(0).getValor();
             Object [] linha = {vc.getArrayLivros(Integer.parseInt(codigo.getText())).get(0).getTitulo()};
            modelo.addRow(linha);
            Livro l1 = vc.getArrayLivros(Integer.parseInt(codigo.getText())).get(0);
            lista.add(l1);
            vc.excluirLivro(Integer.parseInt(codigo.getText()));
            
            
          
            
            if(valor.getText().trim().equals("R$")){
                valor.setText(Integer.toString((int) valor2));
            }
            else{
            double valor1 = Double.parseDouble(valor.getText());
            
            double valor3 = valor1+valor2;
            valor.setText(Integer.toString((int) valor3));
            }
                                  
            
            
            
        }
        else{
                    JOptionPane.showMessageDialog(null,"N√£o temos este livro no Estoque.");
                    
                    }
        
        codigo.setText("");
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

  
    
