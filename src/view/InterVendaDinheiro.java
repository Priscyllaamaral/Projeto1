    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if((!recebido.equals("")) && lista.size()>0 && terminado){
        
          
          cliente1 = vc.getCliente(cpf.getText());
          
          int cod = vc.geraCodigo();
        
        
          Venda v = new VendaDinheiro(cod,FormaDePagamento.Avista, cliente1, Double.parseDouble(valor.getText())
                , lista, data);
        
          vc.efetuarVenda(v);
        
        
        JOptionPane.showMessageDialog(null,"Venda Realizada.");
        
        cpf.setText("");
        codigo.setText("");
        valor.setText("");
        recebido.setText("");
        txtTroco.setText("");
       limpaTabela();
       
            try {
                volta();
            } catch (Exception ex) {
                Logger.getLogger(InterVendaDinheiro.class.getName()).log(Level.SEVERE, null, ex);
            }
       
       
        }else{
            JOptionPane.showMessageDialog(null, "Verifique o campo Recebido ou a lista de Compra.");
        
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         
        try{
        
        Double a =  Double.parseDouble(recebido.getText());
         Double b =  Double.parseDouble(valor.getText());
         
        
           
        
        if(lista.size()==0){
            JOptionPane.showMessageDialog(null,"Nenhuma venda contabilizada.");
        
        }
        
               
       
        else if(vc.getCliente(cpf.getText())== null){
            
            
            int i = JOptionPane.showConfirmDialog(null,"Cliente n√£o Cadastrado!\n"
                + "Cadatrar Cliente?");
        if(i == 0){
            CadastrarCliente cc = new CadastrarCliente();
            cc.setInter(vc.getInter());
            cc.setOpcaoVolta(1);
            cc.setCPF(cpf.getText());
            cc.setVisible(Boolean.TRUE);
        
        }
            
            
        
        
        }        
        
        if( a>= b){
            troco.setText("Troco: ");
            txtTroco.setText("R$ "+Double.toString(a-b));
            terminado = true;
        }else{
            JOptionPane.showMessageDialog(null, "Troco inv√°lido.");
        }}catch(Exception ex){
        JOptionPane.showMessageDialog(null, ex.getMessage());}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        
        if(lista.size()!=0){
            int i = JOptionPane.showConfirmDialog(null,"Deseja Cancelar a Venda?");
            if(i == 0){
                
                vc.adicionarArrayLivros(lista);
                limpaTabela();
                cpf.setText("");
                recebido.setText("");
                codigo.setText("");
                valor.setText("");
                txtTroco.setText("");
                
                
                JOptionPane.showMessageDialog(null, "Venda Cancelada.");
                try {
                    volta();
                } catch (Exception ex) {
                    Logger.getLogger(InterVendaDinheiro.class.getName()).log(Level.SEVERE, null, ex);
                }
              
            }
        }else{
        
        cpf.setText("");
        recebido.setText("");
        codigo.setText("");
        valor.setText("");
        limpaTabela();
        JOptionPane.showMessageDialog(null, "Venda Cancelada.");
            try {
                volta();
            } catch (Exception ex) {
                Logger.getLogger(InterVendaDinheiro.class.getName()).log(Level.SEVERE, null, ex);
            }
       
        }
      
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

