    public void limpaTabela(){
        
        while(modelo.getRowCount() > 0){
            modelo.removeRow(0);
        }
        
    }
    
    public void preencheTabela(){
        
        limpaTabela();
        ArrayList<String[]> array = cc.getClientes();
        
        for (int i = 0; i < array.size(); i++) {
            
            Object [] linha = {array.get(i)[0], array.get(i)[1]};
            modelo.addRow(linha);
            
        }
        
        
        
    
    }

