    public void limpaTabela(){
        
        while(modelo.getRowCount() > 0){
            modelo.removeRow(0);
        }
        
    }

    public void setInter(InterfaceController f){
        lc.setInter(f);
        preencheTabela();
    }
    
    public void preencheTabela(){
        
        limpaTabela();
        
       ArrayList<String[]> array = lc.getLivros();
       
        for (int i = 0; i < array.size(); i++) {
             Object [] linha = {array.get(i)[0],
             array.get(i)[1], array.get(i)[2], array.get(i)[3], array.get(i)[4]};
             modelo.addRow(linha);
            
        }
		                                      
			

		}
        
    
    
    
    
    
    
