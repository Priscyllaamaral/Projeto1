    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         
        if(codigo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo Vazio.");
        }else{
        if(lc.getLivro(Integer.parseInt(codigo.getText()))!= null){
            setVisible(Boolean.FALSE);
           // Livro livro = lc.getLivro(Integer.parseInt(codigo.getText())).get(0);
            String [] livro = lc.getLivro2(Integer.parseInt(codigo.getText()));
            MostrarLivro ml =null;
            try {
                ml = new MostrarLivro();
            } catch (Exception ex) {
                Logger.getLogger(PesquisaLivro.class.getName()).log(Level.SEVERE, null, ex);
            }
            ml.setInter(lc.getInter());
            ml.setLivro(livro);
            ml.setVisible(Boolean.TRUE);
}else{
        
        JOptionPane.showMessageDialog(null,"Não existe Livro com este Código");
        
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

