    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(codigo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo Vazio.");
        }else{
        if(vc.getVenda(Integer.parseInt(codigo.getText())) != null){
            
            
            if(vc.getFormaDePagamento(Integer.parseInt(codigo.getText())) == FormaDePagamento.Avista){
                MostraVenda mv = null;
                try {
                    mv = new MostraVenda();
                } catch (Exception ex) {
                    Logger.getLogger(PesquisaVenda.class.getName()).log(Level.SEVERE, null, ex);
                }
                mv.setInter(vc.getInter());
                mv.setCodigoVenda(Integer.parseInt(codigo.getText()));
             
                
                Object[] ven = vc.getVendaDinheiro(Integer.parseInt(codigo.getText()));
            
                mv.setVenda(ven);
                mv.setVisible(Boolean.TRUE);
        
            }else{
                MostrarVendaCartao mvc = null;
                try {
                    mvc = new MostrarVendaCartao();
                } catch (Exception ex) {
                    Logger.getLogger(PesquisaVenda.class.getName()).log(Level.SEVERE, null, ex);
                }
                mvc.setInter(vc.getInter());
                mvc.setCodigoVenda(Integer.parseInt(codigo.getText()));
                
                
                
                Object[] v = vc.getVendaCartao(Integer.parseInt(codigo.getText()));
                
                mvc.setVenda(v);
                mvc.setVisible(rootPaneCheckingEnabled);
            
            
            }
        
        }else{
        
            JOptionPane.showMessageDialog(null,"NÃ£o existe venda com este cÃ³digo.");
        }
        
        
        
        
        
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

